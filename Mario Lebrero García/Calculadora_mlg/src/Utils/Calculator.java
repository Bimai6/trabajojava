package Utils;

public class Calculator {
    
    public static int plus(int a, int b){
        return a+b;
    }
    
    public static int substract(int a, int b){
        return a-b;
    }
    
    public static int multiply(int a, int b){
        return a*b;
    }
    
    public static int partition(int a, int b){
        return a/b;
    }
}
